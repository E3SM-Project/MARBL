#!/bin/bash

#------------------------------------------------------------------------------

build() {
  CWD=$PWD
  cd ../../drivers
  make $COMPILER
  if [ $? -ne 0 ]; then
    exit
  fi
  cd $CWD
}

#------------------------------------------------------------------------------

run() {
  ../../exe/marbl.exe
}

#------------------------------------------------------------------------------

if [ -z $1 ]; then
  COMPILER=gnu
else
  COMPILER=$1
fi
build
run

